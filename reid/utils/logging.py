from __future__ import absolute_import
import os
import sys

from .osutils import mkdir_if_missing

"""
This code defines a Logger class which can be used to log messages to both the console and a file. 
When an instance of the class is created, it takes an optional argument fpath which is the path to the log file. 
If fpath is not provided, messages will only be logged to the console.
"""
class Logger(object):
    def __init__(self, fpath=None):
        self.console = sys.stdout
        self.file = None
        if fpath is not None:
            mkdir_if_missing(os.path.dirname(fpath))
            self.file = open(fpath, 'w')

    def __del__(self):
        self.close()

    def __enter__(self):
        pass

    def __exit__(self, *args):
        self.close()

    def write(self, msg):
        self.console.write(msg)
        if self.file is not None:
            self.file.write(msg)

    def flush(self):

        self.console.flush()
        if self.file is not None:
            self.file.flush()
            os.fsync(self.file.fileno())

    def close(self):
        self.console.close()
        if self.file is not None:
            self.file.close()
